1. xs ++ [] = xs
基底部:
  [] ++ [] -- ++を適用
= []

再帰部:
  (x:xs) ++ []
= x : (xs ++ []) -- 定義より
= x : xs -- ++を適用
= x:xs
以上.

2. xs ++ (ys ++ zs) = (xs ++ ys) ++ zs
基底部:
  [] ++ (ys ++ zs)
= ys ++ zs -- 定義より
= ([] ++ ys) ++ zs -- リストの性質より


再帰部:
  (x:xs) ++ (ys ++ zs)
= x : (xs ++ (ys ++ zs)) -- 定義より
= x : ((xs ++ ys) ++ zs) -- 仮定より
= x:(xs ++ ys) ++ zs -- ++を逆適用
= (x:xs ++ ys) ++ zs  -- ++を逆適用
以上.
